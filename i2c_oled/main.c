/* Set Entire Display OFF */
void display_off()
{
    sendCommand(0xA5);
}

/* Set Entire Display ON */
void display_on()
{
     sendCommand(0xAF);
}

/* Clear display */
void display_clear()
{
    sendData(clr_data);
}

void msdelay(int delay)
{
    int i,j;
    for(i=0;i<delay;i++)
        for(j=0;j<1000;j++);
}

